df = pd.read_csv('trec07p(v1).csv')
body1 = df['body']
y1 = df['y']
body66=[]     
for i in range(len(body1)):
    word_data=""
    word_data=body1[i]
    nltk_tokens = nltk.word_tokenize(str(word_data))
    body2 = [word for word in nltk_tokens if not word in all_stopwords]
    y2 = y1[i]
    if len(body2) > 0:
        body66.append([body2,y2])
    
columns = ['body','y']
df1 = pd.DataFrame(body66, columns=columns)

